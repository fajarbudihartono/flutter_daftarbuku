class Book {
  final String title;
  final String writer;
  final String price;
  final String image;
  final String description;
  final int pages;
  final double rating;

  Book({
    required this.title,
    required this.writer,
    required this.price,
    required this.image,
    required this.description,
    required this.pages,
    required this.rating,
  });
}
